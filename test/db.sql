 use sn1;
create table users(username varchar(55),pwd varchar(255),fn varchar(55),ln varchar(55),email varchar(55),city varchar(55));
create table message(username varchar(55) ,msg nvarchar(255));
create table post(id int primary key auto_increment,username varchar(55) ,post text);
create table comment(idpost int,username nvarchar(55));
create table likes(idpost int,username nvarchar(55));




















































































































